#include<iostream>
using namespace std;
int main(){
	cout<<"FOR LOOP "<<endl;
	for(int i=1;i<=10;i++){
		cout<<i<<endl;
	}
	cout<<"WHILE LOOP"<<endl;
	int i=1;
	while(i<=10){
	cout<<i<<endl;
	i++;
	}
	cout<<"DO-WHILE LOOP"<<endl;
	
	int j=1;
	do{
	cout<<j<<endl;
		j++;
	}while(j<=10);	
	
}
